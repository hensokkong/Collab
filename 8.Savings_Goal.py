# Define the constants
weekly_savings = 50
weeks = 12
one_time_purchase = 100

# Calculate total accumulation and net savings
total_saved = weekly_savings * weeks
final_savings = total_saved - one_time_purchase

# Display the result
print(f"Total saved over {weeks} weeks: ${total_saved}")
print(f"Final savings balance after the purchase: ${final_savings}")
